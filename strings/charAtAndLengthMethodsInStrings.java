package strings;

public class charAtAndLengthMethodsInStrings {
    public static void main(String[] args) {
        String str = "Soumya Priyadarshini";

        System.out.println("character at index 7 : " + str.charAt(7)); //space is also considered as a character
        System.out.println("length of the given string: "+ str.length());   //array have method written as length only without any brackets
    }
}
